                                                            <td>O Sistema foi desenvolvido por nossa equipe do CaterOT para que as Guilds travem batalhas sem que outros jogadores atrapalhem a performace, o sistema permite que 2 Guilds acessem o mapa escolhido, o tempo determinado para cada batalha é padrão (60 minutos), porém o sistema permite que os jogadores se mantenham no local duelando até que outra Guild solicite acesso ao mapa, ou seja se a Guild 1 está duelando com a Guild 2 e já se passaram 60 minutos e a Guild 3 foi la e solicitou guerra com outra Guild automaticamente todos que estão no mapa serão enviados para o templo da cidade natal do personagem, caso contrario as Guilds 1 e 2 continuam a utilizar o mapa.<br><br>
																Após o lider da Guild enviar o convite para a Guild inimiga, o lider da Guild inimiga terá 2 minutos para aceitar, nesse curto periodo de tempo nenhuma Guild pode invitar no mesmo mapa até que o convite seja rejeitado automaticamente, ou seja o mapa já está parcialmente ocupado.<br><br>
																Nosso sistema pode ser configurado pelos lideres de Guild no momento do convite de acordo com as informações abaixo.
